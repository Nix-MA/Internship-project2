# DOCX GENERATION
# ─────────────────────────────────────────────────────────────────────────────

def generate_docx_report(schema: dict) -> Optional[bytes]:
    """
    Generate a DOCX report from a normalized export schema.
    Returns raw bytes on success, None on failure.
    """
    try:
        from docx import Document
        from docx.shared import Pt, RGBColor, Inches
        from docx.enum.text import WD_ALIGN_PARAGRAPH

        doc = Document()

        # Page margins
        for section in doc.sections:
            section.top_margin = Inches(0.75)
            section.bottom_margin = Inches(0.75)
            section.left_margin = Inches(0.9)
            section.right_margin = Inches(0.9)

        summary = schema.get("summary", {})
        ts = schema.get("timestamp", "N/A")
        sid = schema.get("session_id", "N/A")
        grade = summary.get("grade", "N/A")

        # ── Title ──
        title = doc.add_heading("Lumina Assessment — Evaluation Report", 0)
        title.alignment = WD_ALIGN_PARAGRAPH.CENTER
        title.runs[0].font.color.rgb = RGBColor(80, 50, 200)

        sub = doc.add_paragraph(f"Session: {sid}  |  Generated: {ts}")
        sub.alignment = WD_ALIGN_PARAGRAPH.CENTER
        sub.runs[0].font.size = Pt(10)
        sub.runs[0].font.color.rgb = RGBColor(100, 100, 100)

        doc.add_paragraph()  # spacer

        # ── Summary ──
        doc.add_heading("Summary", 1)
        tbl = doc.add_table(rows=1, cols=4)
        tbl.style = "Table Grid"
        hdr = tbl.rows[0].cells
        for cell, text in zip(hdr, ["Grade", "Score", "Percentage", "Correct"]):
            cell.text = text
            cell.paragraphs[0].runs[0].bold = True

        row = tbl.add_row().cells
        row[0].text = grade
        row[1].text = f"{summary.get('total_score', 0)} / {summary.get('max_score', 0)}"
        row[2].text = f"{summary.get('percentage', 0)}%"
        row[3].text = f"{summary.get('correct_count', 0)} / {summary.get('total_questions', 0)}"

        doc.add_paragraph()

        # ── Per-type breakdown ──
        breakdown = summary.get("performance_breakdown", {})
        if breakdown:
            doc.add_heading("Performance by Question Type", 2)
            for qtype, stats in breakdown.items():
                tp = round((stats["earned"] / stats["max"]) * 100) if stats["max"] else 0
                p = doc.add_paragraph(style="List Bullet")
                p.add_run(f"{qtype}: ").bold = True
                p.add_run(f"{stats['earned']}/{stats['max']} marks ({tp}%)  — {stats['count']} question(s)")

        doc.add_page_break()

        # ── Question breakdown ──
        doc.add_heading("Question-by-Question Breakdown", 1)

        for q in schema.get("questions", []):
            idx = q["index"]
            qtype = q["type"]
            score = q["score"]
            max_s = q["max_score"]
            is_corr = q["is_correct"]
            status = "CORRECT" if is_corr else ("PARTIAL" if score > 0 else "INCORRECT")

            # Question heading
            heading = doc.add_heading(level=3)
            run = heading.add_run(f"Q{idx} [{qtype}]  —  {score}/{max_s} marks  —  {status}")
            if is_corr:
                run.font.color.rgb = RGBColor(20, 140, 20)
            elif score > 0:
                run.font.color.rgb = RGBColor(180, 100, 0)
            else:
                run.font.color.rgb = RGBColor(180, 30, 30)

            def _add_line(label: str, text: str, bold_label=True, color=None):
                p = doc.add_paragraph()
                r_label = p.add_run(f"{label}: ")
                r_label.bold = bold_label
                r_text = p.add_run(_trunc(text, 500))
                if color:
                    r_text.font.color.rgb = color
                p.paragraph_format.space_after = Pt(2)

            _add_line("Question", q["question"])
            _add_line("Correct Answer", q["correct_answer"], color=RGBColor(20, 120, 20))
            _add_line("Your Answer", q["user_answer"],
                      color=RGBColor(20, 120, 20) if is_corr else RGBColor(160, 30, 30))

            if q.get("feedback"):
                _add_line("Feedback", q["feedback"])
            if q.get("strengths"):
                _add_line("Strengths", q["strengths"], color=RGBColor(20, 120, 20))
            if q.get("weaknesses"):
                _add_line("Areas to Improve", q["weaknesses"], color=RGBColor(160, 80, 0))
            if q.get("improvements"):
                _add_line("Improvements", q["improvements"])
            if q.get("criteria_scores"):
                crit_text = "  |  ".join(
                    f"{k.replace('_', ' ').title()}: {v}" for k, v in q["criteria_scores"].items()
                )
                _add_line("Rubric", crit_text, color=RGBColor(60, 60, 140))

            doc.add_paragraph()

        buf = io.BytesIO()
        doc.save(buf)
        buf.seek(0)
        return buf.getvalue()

    except Exception as e:
        logger.error(f"DOCX generation failed: {e}", exc_info=True)
        return None


# ─────────────────────────────────────────────────────────────────────────────
# HELPERS
# ─────────────────────────────────────────────────────────────────────────────

def _sanitize(text: str) -> str:
    """Strip control characters and limit length. Never trust raw LLM output."""
    if not isinstance(text, str):
        text = str(text)
    # Remove control chars except newline/tab
    text = re.sub(r"[^\x09\x0A\x20-\x7E\u00A0-\uFFFF]", "", text)
    return text.strip()


def _trunc(text: str, limit: int) -> str:
    """Truncate to limit with ellipsis to prevent overflow."""
    if not text:
        return ""
    return text[:limit] + ("..." if len(text) > limit else "")
