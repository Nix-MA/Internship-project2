"""
utils/exporters.py — In-memory PDF and DOCX report generation.

Accepts ONLY a normalized export schema (see build_export_schema below).
Never writes to disk. All outputs use BytesIO.
On failure, returns None and logs error — does NOT crash the caller.
"""

import io
import re
import logging
from datetime import datetime
from typing import Optional

logger = logging.getLogger("lumina.exporters")


# ─────────────────────────────────────────────────────────────────────────────
# SCHEMA BUILDER
# ─────────────────────────────────────────────────────────────────────────────

def build_export_schema(
    questions: list,
    answers: dict,
    evaluations: list,
    session_id=None,
) -> dict:
    """
    Normalize raw session state into a portable export schema.
    Keys are sanitized; missing fields default gracefully.
    """
    earned = sum(e.get("score", 0) for e in evaluations)
    max_marks = sum(q.get("marks", 1) for q in questions)
    pct = round((earned / max_marks) * 100, 1) if max_marks else 0.0
    grade = (
        "S" if pct >= 90 else
        "A" if pct >= 80 else
        "B" if pct >= 70 else
        "C" if pct >= 50 else
        "D"
    )

    # Per-type performance breakdown
    type_breakdown: dict = {}
    for i, q in enumerate(questions):
        t = q.get("type", "Unknown")
        ev = evaluations[i] if i < len(evaluations) else {}
        if t not in type_breakdown:
            type_breakdown[t] = {"earned": 0, "max": 0, "count": 0}
        type_breakdown[t]["earned"] += ev.get("score", 0)
        type_breakdown[t]["max"] += q.get("marks", 1)
        type_breakdown[t]["count"] += 1

    normalized_questions = []
    for i, q in enumerate(questions):
        ev = evaluations[i] if i < len(evaluations) else {}
        ans = answers.get(i, "")
        if isinstance(ans, dict):
            ans = str(ans)
        normalized_questions.append({
            "index": i + 1,
            "question": _sanitize(q.get("question", "")),
            "type": _sanitize(q.get("type", "Unknown")),
            "user_answer": _sanitize(str(ans) if ans else "[No answer provided]"),
            "correct_answer": _sanitize(str(q.get("correct_answer", q.get("answer", "N/A")))),
            "score": ev.get("score", 0),
            "max_score": q.get("marks", 1),
            "is_correct": bool(ev.get("is_correct", False)),
            "feedback": _sanitize(ev.get("feedback", "")),
            "hint": _sanitize(ev.get("hint", "")),
            "strengths": _sanitize(ev.get("strengths", "")),
            "weaknesses": _sanitize(ev.get("weaknesses", "")),
            "improvements": _sanitize(ev.get("improvements", "")),
            "criteria_scores": {
                _sanitize(k): v
                for k, v in ev.get("criteria_scores", {}).items()
            },
        })

    return {
        "session_id": str(session_id) if session_id else "N/A",
        "timestamp": datetime.now().strftime("%Y-%m-%d %H:%M"),
        "questions": normalized_questions,
        "summary": {
            "total_score": earned,
            "max_score": max_marks,
            "percentage": pct,
            "grade": grade,
            "total_questions": len(questions),
            "correct_count": sum(1 for e in evaluations if e.get("is_correct")),
            "performance_breakdown": type_breakdown,
        },
    }


