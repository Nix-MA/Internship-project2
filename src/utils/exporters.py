def generate_pdf_report(schema: dict) -> Optional[bytes]:
    """
    Generate a PDF report from a normalized export schema.
    Returns raw bytes on success, None on failure.
    All text is sanitized and truncated to prevent overflow.
    Uses fpdf2 v2.7.x API.
    """
    try:
        from fpdf import FPDF
        from fpdf.enums import XPos, YPos

        class LuminaPDF(FPDF):
            def header(self):
                self.set_font("Helvetica", "B", 10)
                self.set_text_color(120, 80, 200)
                self.cell(0, 8, "Lumina Assessment - Evaluation Report", align="C",
                          new_x=XPos.LMARGIN, new_y=YPos.NEXT)
                self.set_draw_color(80, 50, 140)
                self.set_line_width(0.5)
                self.line(10, self.get_y(), 200, self.get_y())
                self.ln(4)

            def footer(self):
                self.set_y(-12)
                self.set_font("Helvetica", "I", 8)
                self.set_text_color(150, 150, 150)
                self.cell(0, 8, f"Page {self.page_no()} | Generated by Lumina Assessment", align="C")

        def latin_safe(t):
            """Transliterates common unicode chars and strips the rest for latin-1 font support."""
            if not isinstance(t, str):
                t = str(t)
            reps = {
                '\u2018': "'", '\u2019': "'", '\u201c': '"', '\u201d': '"',
                '\u2013': "-", '\u2014': "-", '\u2026': "...", '\u00A0': " ",
                '\u2022': "-", '\u00B7': "-"
            }
            for k, v in reps.items():
                t = t.replace(k, v)
            return t.encode('latin-1', 'replace').decode('latin-1')

        def cell_nl(p, w, h, text, **kw):
            """Helper: cell with automatic newline."""
            p.cell(w, h, latin_safe(text), new_x=XPos.LMARGIN, new_y=YPos.NEXT, **kw)

        def mcell(p, text):
            """Helper: multi_cell with safe defaults."""
            # Strip newlines that confuse fpdf within a single field
            safe = text.replace("\r", " ").replace("\n", " ")
            p.multi_cell(0, 5, latin_safe(safe), new_x=XPos.LMARGIN, new_y=YPos.NEXT)

        pdf = LuminaPDF()
        pdf.set_auto_page_break(auto=True, margin=15)
        pdf.add_page()

        summary = schema.get("summary", {})
        ts  = schema.get("timestamp", "N/A")
        sid = schema.get("session_id", "N/A")

        # ── Title block ──
        pdf.set_font("Helvetica", "B", 20)
        pdf.set_text_color(80, 50, 200)
        cell_nl(pdf, 0, 12, "Lumina Assessment", align="C")
        pdf.set_font("Helvetica", "", 11)
        pdf.set_text_color(80, 80, 80)
        cell_nl(pdf, 0, 7, f"Session: {sid}  |  Generated: {ts}", align="C")
        pdf.ln(6)

        # ── Summary box ──
        grade = summary.get("grade", "N/A")
        pdf.set_fill_color(240, 235, 255)
        pdf.set_draw_color(150, 100, 240)
        pdf.set_font("Helvetica", "B", 13)
        pdf.set_text_color(60, 20, 160)
        summary_text = (
            f"Overall Grade: {grade}  |  "
            f"Score: {summary.get('total_score', 0)} / {summary.get('max_score', 0)}  |  "
            f"{summary.get('percentage', 0)}%"
        )
        cell_nl(pdf, 0, 10, summary_text, border=1, align="C", fill=True)
        pdf.ln(3)

        pdf.set_font("Helvetica", "", 10)
        pdf.set_text_color(50, 50, 50)
        cell_nl(pdf, 0, 7,
                f"Total Questions: {summary.get('total_questions', 0)}  |  Correct: {summary.get('correct_count', 0)}")
        pdf.ln(4)

        # ── Per-type breakdown ──
        breakdown = summary.get("performance_breakdown", {})
        if breakdown:
            pdf.set_font("Helvetica", "B", 11)
            pdf.set_text_color(60, 20, 160)
            cell_nl(pdf, 0, 8, "Performance by Question Type")
            pdf.set_font("Helvetica", "", 10)
            pdf.set_text_color(50, 50, 50)
            for qtype, stats in breakdown.items():
                tp  = round((stats["earned"] / stats["max"]) * 100) if stats["max"] else 0
                line = f"  {_trunc(qtype, 30)}: {stats['earned']}/{stats['max']} marks ({tp}%) - {stats['count']} question(s)"
                cell_nl(pdf, 0, 6, line)
            pdf.ln(4)

        pdf.set_draw_color(200, 200, 200)
        pdf.set_line_width(0.3)
        pdf.line(10, pdf.get_y(), 200, pdf.get_y())
        pdf.ln(5)

        # ── Question breakdown ──
        pdf.set_font("Helvetica", "B", 12)
        pdf.set_text_color(60, 20, 160)
        cell_nl(pdf, 0, 8, "Question-by-Question Breakdown")
        pdf.ln(2)

        for q in schema.get("questions", []):
            idx     = q["index"]
            qtype   = q["type"]
            score   = q["score"]
            max_s   = q["max_score"]
            is_corr = q["is_correct"]
            status  = "CORRECT" if is_corr else ("PARTIAL" if score > 0 else "INCORRECT")

            # Question header
            pdf.set_fill_color(245, 242, 255)
            pdf.set_draw_color(180, 150, 240)
            pdf.set_font("Helvetica", "B", 10)
            pdf.set_text_color(40, 10, 120)
            cell_nl(pdf, 0, 8,
                    f"Q{idx} [{qtype}]  -  {score}/{max_s} marks  -  {status}",
                    border=1, fill=True)

            pdf.set_font("Helvetica", "", 9)
            pdf.set_text_color(30, 30, 30)
            mcell(pdf, f"Question: {_trunc(q['question'], 400)}")

            pdf.set_text_color(20, 100, 20)
            mcell(pdf, f"Correct Answer: {_trunc(q['correct_answer'], 300)}")

            user_color = (20, 100, 20) if is_corr else (160, 30, 30)
            pdf.set_text_color(*user_color)
            mcell(pdf, f"Your Answer: {_trunc(q['user_answer'], 300)}")

            if q.get("feedback"):
                pdf.set_text_color(80, 80, 80)
                mcell(pdf, f"Feedback: {_trunc(q['feedback'], 300)}")

            if q.get("strengths"):
                pdf.set_text_color(20, 120, 20)
                mcell(pdf, f"Strengths: {_trunc(q['strengths'], 200)}")

            if q.get("weaknesses"):
                pdf.set_text_color(160, 80, 0)
                mcell(pdf, f"Areas to Improve: {_trunc(q['weaknesses'], 200)}")

            if q.get("criteria_scores"):
                pdf.set_text_color(60, 60, 120)
                crit_text = "  |  ".join(
                    f"{k.replace('_', ' ').title()}: {v}"
                    for k, v in q["criteria_scores"].items()
                )
                mcell(pdf, f"Rubric: {_trunc(crit_text, 300)}")

            pdf.set_text_color(50, 50, 50)
            pdf.ln(3)

        buf = io.BytesIO()
        pdf.output(buf)
        buf.seek(0)
        return buf.getvalue()

    except Exception as e:
        logger.error(f"PDF generation failed: {e}", exc_info=True)
        return None


